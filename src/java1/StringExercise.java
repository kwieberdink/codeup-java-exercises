package java1;

public class StringExercise {

    public static void main(String[] args) {

        String output1 = "We don't need no education\n" +
                "We don't need no thought control\n";
        System.out.println(output1);

        String output2 = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(output2);

        String output3 = "In windows, the main drive is usually C:\\\n";
        System.out.println(output3);

        String output4 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(output4);

    }

}
